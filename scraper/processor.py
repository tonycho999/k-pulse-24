def parse_rankings(raw_rankings_text, category):
    """í…ìŠ¤íŠ¸ í˜•íƒœì˜ ë­í‚¹ì„ DB ê°ì²´ ë¦¬ìŠ¤íŠ¸ë¡œ ë³€í™˜"""
    if not raw_rankings_text: return []
    parsed = []
    # ë§ˆí¬ë‹¤ìš´ ê¸°í˜¸ ë° ë¶ˆí•„ìš”í•œ ê³µë°± ì œê±° í›„ ì¤„ë°”ê¿ˆ ë‹¨ìœ„ë¡œ ë¶„ë¦¬
    lines = raw_rankings_text.replace('*', '').strip().split('\n')
    for i, line in enumerate(lines):
        if i >= 10: break
        try:
            # ì£¼ì„([1]) ì œê±° ë° í´ë¦¬ë‹
            line = re.sub(r'\[\d+\]', '', line).strip()
            # ìˆ«ìì™€ ê¸°í˜¸ ì œê±° í›„ ìˆœìˆ˜ ì œëª© ì¶”ì¶œ
            title = re.sub(r'^\d+[\.\)\s-]*', '', line).strip()
            if title:
                parsed.append({
                    "category": category,
                    "rank": i + 1,
                    "title_en": title,
                    "title_kr": title,
                    "score": 100 - (i * 5),
                    "created_at": datetime.now().isoformat()
                })
        except: continue
    return parsed

